import random

class דמות:
    def __init__(self, שם, בריאות, התקפה):
        self.שם = שם
        self.בריאות = בריאות
        self.התקפה = התקפה
        self.זהב = 0
        self.כוח = 0

    def חי(self):
        return self.בריאות > 0

    def קח_נזק(self, נזק):
        self.בריאות -= נזק
        print(f"{self.שם}: ירדו לך {נזק} חיים! החיים שלך עכשיו {self.בריאות}.")

    def קנה_חיים(self, כמות):
        self.בריאות += כמות
        print(f"{self.שם}: קנית {כמות} חיים! החיים שלך עכשיו {self.בריאות}.")

    def קנה_התקפה(self, כמות):
        self.התקפה += כמות
        self.כוח += כמות
        print(f"{self.שם}: קנית {כמות} התקפה! הכוח שלך עכשיו {self.כוח}.")


