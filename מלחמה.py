def קרב(שחקן, מפלצת):
    while שחקן.חי() and מפלצת.חי():
        try:
            פעולה = int(input("אם אתה רוצה לתקוף אותו הקש 1, אם אתה רוצה לברוח הקש 2: ").strip())
            if פעולה == 1:
                מפלצת.קח_נזק(שחקן.התקפה)
                if מפלצת.חי():
                    שחקן.קח_נזק(מפלצת.התקפה)
            elif פעולה == 2:
                ריפוי = random.randint(10, 20)
                שחקן.קנה_חיים(ריפוי)
                print(f"{שחקן.שם} ברחת וקיבלת {ריפוי} חיים!")
                break
            else:
                print("נא להקיש 1 או 2 בלבד.")
        except ValueError:
            print("נא להקיש מספר בלבד.")


